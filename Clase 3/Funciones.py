"""
Ejemplo de funciones en Python.
"""
a = 5
b = 6
def sumar(a, b):
    return a + b  # Retorna la suma de dos números


print("Resultado de la suma:", sumar(a, b))  # Imprime el resultado de la suma


def restar(a, b):
    return a - b  # Retorna la resta de dos números


print("La resta es:", restar(a, b))  # Imprime el resultado de la resta