"""
Ejemplo estructura de control For en Python.
"""
array = ["futbol", "Pc", 18.6, 18, [6, 7, 10, 4], True, False]
print(len(array))  # Imprime el contenido de la lista
for i in range(len(array)):  # Itera desde 0 hasta 5
    print("Valor de i:", i)  # Imprime el valor actual de i
print("Fin del bucle for")  # Mensaje al finalizar el bucle
